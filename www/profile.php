<h2>Attorney Profile</h2>

<img src="img/jmt.jpg" width="200" class="john clearfix">

<p>John M. Trani is the principal of The Law Office of John M. Trani. Mr. Trani focuses his practice on
intellectual property and brand management, business and entertainment transactions, immigration matters
and general litigation. In addition, Mr. Trani is the General Counsel of <a href="http://www.carrotcreative.com" target="_blank">Carrot Creative, LLC</a>, a digital and
new-media marketing agency focusing on social media.</p>

<p>Before founding his own law firm, Mr. Trani was an associate at Ross &amp; Asmar LLC, a general practice.
During his time at Ross &amp; Asmar LLC, Mr. Trani litigated a variety of cases and counseled on transactions
involving intellectual property, business, immigration and labor and employment matters.</p>

<p>He also served as a law clerk at the Federal Department of Labor and for the Honorable William Macauley
at the Federal Equal Employment Opportunity Commission.</p>

<p>Moreover, he has worked on various corporate, intellectual property and entertainment transactions in
the legal departments of DC Comics and Blitz Distribution, Inc., a leading manufacturer of skateboard
equipment and apparel, and at multiple entertainment law firms, including Carroll, Guido &amp; Groffman
LLP.</p>

<p>Mr. Trani earned his law degree from Brooklyn Law School. Prior to law school, Mr. Trani graduated from
the Boston University School of Management, where he earned a B.S. in business administration with a
concentration in finance. Mr. Trani also completed Dean's List coursework at Purdue University, Villanova
University, and Southwestern Law School.</p>

<p>Mr. Trani is admitted to practice before the federal and state courts of New York. Mr. Trani is a member
of the New York State Bar Association, New York County Lawyersâ€™ Association and the Brooklyn Bar
Association.</p>
